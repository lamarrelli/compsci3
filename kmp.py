def kmp(s):
    n = len(s)
    fl = [0] * n

    j = 0
    for i in range(1, n):
        while j > 0 and s[i] != s[j]:
            j = fl[j - 1]
        if s[i] == s[j]:
            j += 1
        fl[i] = j

    return fl

